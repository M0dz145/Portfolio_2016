        <section class="veille-technologique" data-reveal-section="veille-technologique">
            <h2><span>Méthodologies</span> design web</h2>

            <div class="page">
                <h3>Sommaire</h3>
                <ol>
                    <li>Introduction</li> <!-- <a href="#vt-intro"> -->
                    <li>OOCSS</li>
                    <li>BEM</li>
                    <li>Atomic Design</li>
                    <li>SASS/SCSS</li>
                    <li>Conclusion</li>
                </ol>
            </div>

            <div class="page">
                <h3>1. Introduction</h3>
                <p>
                    Depuis plusieurs années, je développe des sites et applications web pour des particuliers et
                    professionnels. Les projets prenant de l'ampleur, je me suis intéressé aux différentes méthodologies
                    et architectures existantes afin de maintenir au mieux le design de mes produits.
                </p>
                <p>
                    Du nommage des classes à l'architecture des feuilles de style en passant par les technologies et
                    outils, nous verrons comment <u>entretenir un design d'application web</u> avec ces derniers.
                </p>
            </div>

            <div class="page">
                <h3>2. OOCSS</h3>
                <p>
                    OOCSS, l'acronyme de « Object Oriented CSS », est un ensemble de bonnes pratiques initiées par
                    Nicole Sullivan.
                </p>
                <p>
                    Le concept de l'OOCSS est de repérer les « objets CSS » afin de créer des classes réutilisables.
                    <br>
                    La particularité de cette méthodologie est de prendre le design comme point de départ. Dans un
                    premier temps, on repère les répétitions visuelles, puis on les nomme.
                </p>
                <p>
                    Lorsque la pratique OOCSS est bien faite, il est possible d'ajouter des morceaux entiers dans le
                    design sans toucher à la feuille de style CSS. Il suffit donc d'utiliser les objets CSS déjà
                    existants.
                </p>
                <figure>
                    <img class="full-width" src="img/veille-technologique/Design_des_Halles_de_Paris.jpg" alt="OOCSS">
                    <figcaption>Design des Halles de Paris, 1863, par Victor Baltard</figcaption>
                </figure>

                <p>
                    OOCSS met en avant deux principes:
                </p>
                <ol class="list--inner">
                    <li>Le principe de séparation de la structure et de l'apparence</li>
                    <li>Le principe de séparation du conteneur et du contenu</li>
                </ol>
                <p>
                    Le premier principe nous fera préférer, dans les sélecteurs CSS, les classes plutôt que des
                    identifiants ou des noms d'éléments HTML. Il incite également à factoriser les propriétés visuelles
                    répétées. Pour l'exemple, partons du code HTML suivant:
                </p>
                <pre>
                    <code class="html">
&lt;button class="small-btn">&lt;/button>
&lt;button class="large-btn">&lt;/button>
                    </code>
                </pre>
                <p>
                    Il est préférable de factoriser dans une classe <code>btn</code> les règles CSS communes aux deux
                    boutons, ce qui donne:
                </p>
                <pre>
                    <code class="html">
&lt;button class="btn small-btn">&lt;/button>
&lt;button class="btn large-btn">&lt;/button>
                    </code>
                </pre>

                <p>
                    Le second principe consiste à éviter des cascades CSS comme <code>.links-box .title</code>, car
                    l'apparence du
                    contenu <code>.title</code> serait alors couplée au conteneur <code>.links-box</code>. Une classe
                    <code>.box-title</code> sera plus réutilisable.
                </p>
            </div>

            <div class="page">
                <h3>3. BEM</h3>
                <p>
                    La méthodologie BEM, l'acronyme de « Block Element Modifier », est une solution élaborée par Yandex
                    publiée en 2010. Elle apporte une convention de nommage des classes CSS et peut se marier
                    parfaitement à la méthodologie OOCSS.
                </p>
                <p>
                    Le concept est simple: tout ce qui compose une page ou une application Web peut toujours être rangé
                    dans
                    une arborescence de <u>blocs</u>, d'<u>éléments</u> et de
                    <u>modificateurs</u>.
                </p>

                <p>
                    Un bloc est une entité indépendante, une « brique » de l'application ou de la page Web. Un bloc
                    forme son propre contexte autonome. Ci-dessous des exemples de blocs dans une illustration tirée du
                    site officiel:
                </p>
                <figure>
                    <img class="full-width" src="img/veille-technologique/bem-blocks.jpg" alt="BEM">
                    <figcaption>Des blocs BEM</figcaption>
                </figure>

                <p>
                    Un élément est une partie d'un bloc. Le contexte d'un élément est celui du bloc. Ci-dessous deux
                    exemples empruntés toujours au site officiel:
                </p>
                <figure>
                    <img class="full-width" src="img/veille-technologique/bem-elements.jpg" alt="BEM">
                    <figcaption>Des éléments BEM</figcaption>
                </figure>

                <p>
                    Un modificateur est une propriété qui sert à créer des variantes, pour faire des modifications
                    minimes comme changer des couleurs. Il existe des <u>modificateurs de blocs</u> et des
                    <u>modificateurs d'éléments</u>.
                </p>

                <hr>

                <p>
                    La méthodologie BEM établit ensuite trois règles essentielles:
                </p>
                <ol class="list--inner">
                    <li>
                        Les blocs et les éléments doivent chacun avoir un nom unique, lequel sera utilisé comme classe
                        CSS,
                    </li>
                    <li>Les sélecteurs CSS ne doivent pas utiliser les noms des éléments HTML (pas de .menu td),</li>
                    <li>Les cascades dans les sélecteurs CSS devraient être évitées.</li>
                </ol>

                <hr>

                <p>
                    L'équipe de BEM utilise pour sa part une syntaxe à base de <i>underscores</i>:
                </p>
                <pre>
                    <code class="css">
.block-name {}
.block-name_modifier_name {}
.block-name__element-name {}
.block-name__element-name_modifier_name {}
                    </code>
                </pre>

                <hr>

                <p>
                    Le site officiel prend soin de préciser que seuls comptent les concepts, la syntaxe restant libre.
                    Suite à celà, j'ai moi-même décidé d'une convention de nommage plus claire à mon goût:
                </p>
                <pre>
                    <code class="css">
.blockName {}
.blockName--modifierName {}
.blockName__elementName {}
.blockName__elementName--modifierName {}
                    </code>
                </pre>

                <p>
                    BEM permet de ne jamais aboutir à un code comme celui-ci:
                </p>
                <pre>
                    <code class="css">
.my-aside-title {
  font-size: 1.5em; /* Je style mon titre, OK c'est propre */
}
.my-aside h2.my-aside-title {
  position: inherit; /* PROBLEME: Annulation d'une règle sur h2 prévue pour autre chose */
}
                    </code>
                </pre>

                <p>
                    Voici un exemple de l'utilisation de BEM dans une structure HTML:
                </p>
                <pre>
                    <code class="html">
&lt;div class="bodyLayout">
    &lt;main class="bodyLayout__mainContent">
        &lt;article class="mainContent">&lt;!-- Content here… -->&lt;/article>
    &lt;/main>
    &lt;div class="bodyLayout__sidebar sidebar">&lt;!-- Widgets here… -->&lt;/div>
&lt;/div>
                    </code>
                </pre>

            </div>

